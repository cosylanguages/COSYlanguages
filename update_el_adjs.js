const match = content.match(/const data = (\[[\s\S]*?\]);/);
if (match) {
    const data = eval(match[1]);
    data.forEach(item => {
        const key = item.word.toLowerCase();
        if (mapping[key]) {
            item.subtext = mapping[key];
        }
        // Remove second definition if English
        if (item.definitions && item.definitions.length > 1) {
             if (/^[A-Za-z]/.test(item.definitions[1].text)) {
                 item.definitions.splice(1, 1);
             }
        }
    });
    const newData = JSON.stringify(data, null, 4);
    content = content.replace(/const data = \[[\s\S]*?\];/, `const data = ${newData};`);
    fs.writeFileSync(filePath, content);
}
